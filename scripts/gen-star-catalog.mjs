// Regenerates src/star-catalog.ts from the d3-celestial bright-star data
// (Yale BSC derived, public domain). Run: node scripts/gen-star-catalog.mjs
// Keeps stars to magnitude <= 3.6 (~330) so the embedded table stays ~2 kB
// gzipped while covering every naked-eye constellation figure.

import { writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const SOURCE =
	"https://raw.githubusercontent.com/ofrohn/d3-celestial/master/data/stars.6.json";
const MAG_LIMIT = 3.6;

const res = await fetch(SOURCE);
if (!res.ok) throw new Error(`fetch failed: ${res.status}`);
const data = await res.json();

const rows = [];
for (const f of data.features) {
	const mag = f.properties?.mag;
	const [lon, dec] = f.geometry?.coordinates ?? [];
	if (typeof mag !== "number" || mag > MAG_LIMIT) continue;
	if (typeof lon !== "number" || typeof dec !== "number") continue;
	const ra = lon < 0 ? lon + 360 : lon; // catalog stores RA as -180..180
	rows.push([Math.round(ra * 10), Math.round(dec * 10), Math.round(mag * 10)]);
}
rows.sort((a, b) => a[2] - b[2]); // brightest first

const flat = rows.flat().join(",");
const out = `// Generated by scripts/gen-star-catalog.mjs — do not edit by hand.
// ${rows.length} stars to magnitude ${MAG_LIMIT} (Yale BSC via d3-celestial,
// public domain). Flat triples: [ra*10 deg, dec*10 deg, mag*10], brightest first.

export const STAR_CATALOG: readonly number[] = [${flat}];

export const STAR_COUNT = ${rows.length};
`;

const here = dirname(fileURLToPath(import.meta.url));
const target = resolve(here, "../src/star-catalog.ts");
writeFileSync(target, out);
console.log(`wrote ${target}: ${rows.length} stars, ${out.length} bytes`);
